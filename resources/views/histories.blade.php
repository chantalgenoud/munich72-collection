            <p style="font-size:large"><br>

              The history of the design of the 1972 Olympic Games begins with a rather unspectacular reunion between Otl Aicher and Tomas Nittner in the mid-1960s. Nittner, who was born in the Czech Republic, was a former student at the Hochschule für Gestaltung in Ulm (HfG), had studied with Aicher from 1961 to 1964. After leaving the HfG, he opened a small design office in Munich and was approached at the end of 1965, at the age of 23, by Aicher, who was in his early 40s at the time, with a view to working together at the Munich Games.<br><br>

              Since Aicher continued to run his office in Ulm and Nittner was still based in Munich, they met at the weekends in the "Roter Hof", Aicher's retreat in the forest near Kissleg, about 60 kilometers west of Munich. They spent their time, as Nittner recalls, "dreaming ideas." Aicher wanted to prepare for a meeting with the President of the Olympic Organizing Committee, Willi Daume - a meeting that would eventually take place around the spring of 1966 at the HfG Ulm. That was about a month before Munich would award the games. Florian Aicher, son of Otl Aicher, remembers: “The Rote Hof has been our family’s summer home since the early 1960s. Family friends, colleagues, employees of my father's office, etc. were often guests there. The meetings there were informal, we played, and laughed. <br><br>

              The weekend meetings of the "troupe", as Nittner called the group of employees, were informal get-togethers. In the meantime, a few employees from Aicher's office had joined, including Alfred Kern. "Aicher always tried to be the primus inter pares, a first among equals, and not the grand boss," remembers Nittner. On October 7, 1966, Aicher was officially appointed visual design officer for the games. And at the beginning of 1967, Aicher asked his colleague Nittner to look for a studio in Munich.<br><br>

              Nittner found a suitable office on the second floor of a new industrial building in Garching-Hochbrück, just twelve kilometers north of Munich. It was a large, open room that Aicher and Nittner divided up with white-painted wooden panels. Aicher designed desks that consisted of an untreated steel frame and hardboard. They moved in at the beginning of April 1967.<br><br>

              Documents in the Ulm archive show that from April to June of this first year, Aicher's team comprised a total of eight people, including Aicher himself: the designers Thomas Nittner, Alfred Kern, and Rolf Müller as well as the technical draftsmen András Dózsa-Farkas, Wolfgang Hirschfeld, Gerhard Joksch and the secretary Susanne Osterried.<br><br>

              In July 1968, Aicher's team in Garching was integrated into the organizing committee as Dept. XI. Nine months later, in April 1969, after a two-year stay, the team was moved to the office of the organizing committee at Saarstrasse 7, within walking distance, where they stayed until February 1972 and finally found their place in a room within the OC offices in the Olympic Village. There they remained throughout the Games and for some time beyond. With the move to Saarstrasse, Aicher and his employees lost their self-perceived autonomy. They now worked within the committee's office and were subject to daily scrutiny.<br><br>

              Aicher's team grew. Ultimately, it had eight project managers, each of whom was assigned their own area of ​​responsibility. Rolf Müller, Aicher's deputy, was responsible for advertising and publications, Gerhard Joksch for sports pictograms and posters, Alfred Kern for information design, information pictograms, and signage, Ian McLaren for the design of the cultural program, Hans Roericht for 3D and Olympic village facilities, Elena Winschermann for the Mascot of the Games. Vera Simmert took care of the clothing for all official employees in and around the sports facilities, Eberhard Stauss planned the city decoration. In addition to the eight, Holger Franke coordinated the work of the Dept. XI team in Kiel, the location of the yachting events.<br><br>

              Department XI included several departments - design, photography, technical representation, illustration, and graphics. There were photo lab assistants, a studio manager, and secretarial staff. The team worked alongside other teams that at first glance appeared to be part of Division XI but technically weren't. In addition, Hans (Nick) Roericht and colleagues work on various stadium and equipment projects under the name "Working Group Equipment". Although Roericht's team shared desks with Department XI, while he maintained his own commercial studio throughout the games, spending only two days a week in Munich.<br><br>

              In addition, there was the Augsburg agency Ay und Breuer (Hermann Ay and Josef Breuer), which retained its autonomy. The agency partners were alumni of HfG Ulm and produced many, if not all, promotional communications, promotional leaflets, and the like, including some literature for the Augsburg slalom canoe events.<br><br>

              Department XI, including the Schwaiger and Roericht teams and all independent contractors, totaled 82 people throughout the project. This includes designers, designers in supporting roles, technical staff, studio manager Friedhelm Brebeck, and about six secretarial staff as well as people who worked in Kiel and Augsburg.<br><br>

              The team of Dept. XI was international and relatively young. In addition to Germans, there were also Algerian, British, Czechoslovakian, Dutch, French, Hungarian, Italian, and Swiss nationals. In 1970, at the beginning of the peak phase of the project, Rolf Müller had turned 30, and the team bosses were all in their thirties. Older team members included Walter Schwaiger (34), Friedhelm Brebeck (36) and Hans Roericht (38). On the other hand, Vera Simmert (25) and Elena Winschermann (21) were significantly younger than the others.<br><br>

              Although Aicher approached former HfG employees to hire designers, the number of people in Ulm he worked with may not be as large as is sometimes assumed. Of those in a creative capacity who were employed in the various forms of the Olympic team from 1965 to 1973, only twelve came from the HfG Ulm, including the photographer Karsten de Riese. Only seven former Ulm students worked full-time in the offices of Dept. XI in Munich during the same period.

              After the games in Munich, part of the team in Aicher's studio moved to Rotis. Barbara Adam, Andreas Fahrni, Petra Kahlenberg, Alfred Kern, Hans Roericht and Elena Winschermann initially worked there.
